// Write a C++ program to display the first 10 natural numbers.

#include"iostream"
using namespace std;

int main()
{
    int i;

    cout<< "First 10 natural numbers are : \n";

    for(i = 1; i <= 10; i++)
    {
        cout<< i << endl;
    }

    return 0;
}

